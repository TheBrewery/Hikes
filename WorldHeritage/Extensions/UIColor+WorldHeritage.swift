import UIKit

extension UIColor {
    class func whDarkBlueColor() -> UIColor {
//        rgb 7 100 167
        return UIColor.fromHex("0764A7")
    }

    class func whRedColor() -> UIColor {
//        rgb 255 89 89
        return UIColor.fromHex("FF5959")
    }
    
    class func whDarkGreenColor() -> UIColor {
//        rgb 12 140 5
        return UIColor.fromHex("0C8C05")
    }
}
