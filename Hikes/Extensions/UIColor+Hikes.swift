import UIKit

extension UIColor {
    class func tbLightGreenColor() -> UIColor {
        return UIColor(red:0.28, green:0.56, blue:0, alpha:1)
    }
    
    class func tbDarkGreenColor() -> UIColor {
        return UIColor(red:0.0, green:0.5, blue:0, alpha:1)
    }
}
